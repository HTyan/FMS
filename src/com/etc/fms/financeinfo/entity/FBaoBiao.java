package com.etc.fms.financeinfo.entity;

public class FBaoBiao {

	private String cname;
	private int cid;
	private double ys;
	private double sh;

	public int getCid() {
		return cid;
	}

	public void setCid(int cid) {
		this.cid = cid;
	}

	public String getCname() {
		return cname;
	}

	public void setCname(String cname) {
		this.cname = cname;
	}

	public double getYs() {
		return ys;
	}

	public void setYs(double ys) {
		this.ys = ys;
	}

	public double getSh() {
		return sh;
	}

	public void setSh(double sh) {
		this.sh = sh;
	}


}
